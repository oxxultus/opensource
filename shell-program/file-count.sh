#!/bin/bash
# 현재 디렉토리의 파일 및 디렉토리 개수를 세는 스크립트

# ls -A : 숨김 파일을 포함한 모든 항목을 나열 (단, . 와 .. 는 제외)
# ls -a : 숨김 파일을 포함한 모든 항목을 나열 (단, . 와 .. 포함)
# wc -l : 줄의 개수를 센다 (즉, 항목의 개수)
COUNT=$(ls -A | wc -l)

echo "============================================="
echo "현재 디렉토리의 전체 파일 및 디렉토리 개수: $COUNT"
echo "============================================="